        <h2 class="content-block__title">Atomic Extractability Requirement</h2>
      </div>
      <div class="content-block__body">
        <p>Research shows LLM failures emerge when facts require surrounding context. Those facts are skipped or altered.</p>
        <p>Atomic extractability means:</p>
        <ul>
          <li>One fact per sentence</li>
          <li>No conjunctions ("and", "but", "also") inside factual claims</li>
          <li>Explicit subject and predicate</li>
          <li>No pronouns ("this", "it", "they")</li>
          <li>No implied context</li>
        </ul>
        <p>Isolation test: Copy the sentence alone. If meaning degrades, the sentence fails atomic extractability.</p>
        <p>Why this matters: AI systems extract fragments without preserving context. Facts that depend on context will be misunderstood or ignored.</p>
      </div>
